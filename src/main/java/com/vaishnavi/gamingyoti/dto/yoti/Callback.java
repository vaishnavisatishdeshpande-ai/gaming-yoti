package com.vaishnavi.gamingyoti.dto.yoti;

public class Callback {

    private Boolean auto;
    private String url;

    public Callback() {
    }

    public Boolean getAuto() {
        return auto;
    }

    public void setAuto(Boolean auto) {
        this.auto = auto;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}